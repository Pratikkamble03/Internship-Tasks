#task 17 - create classes cat,dog,cow with same method sound
class Cat:
    def sound(self):
        print("Cat says Meow")
class Dog:
    def sound(self):
        print("Dog says Bark")
class Cow:
    def sound(self):
        print("Cow says Moo")
c = Cat()
d = Dog()
cw = Cow()
c.sound()
d.sound()
cw.sound()
